#include <stdio.h>

int main() {
    // for loop = repeats a section of code a limited amount of times

    for (int i = 1; i <= 10; i++)
    {
      printf("Iteration %d: test\n", i);
    };




        for (int i = 1; i <= 10; i+=3)
    {
      printf("Now we increment by 3. Iteration %d: test\n", i);
    };


    return 0;
};

